uint32_t make32b(uint8_t* buff, uint32_t base)
{
  uint32_t rv;

  rv  = (uint32_t)(buff[base + 0]) <<  0;
  rv += (uint32_t)(buff[base + 1]) <<  8;
  rv += (uint32_t)(buff[base + 2]) << 16;
  rv += (uint32_t)(buff[base + 3]) << 24;

  return rv;
}
